package com.ms.mongowheels.beans;

public class Dimensions
{
    private int Height;
    private int Length;
    private int Width;

    // ----------------- getter and setters ------------------
    public int getHeight()
    {
        return this.Height;
    }

    public int getLength()
    {
        return this.Length;
    }

    public int getWidth()
    {
        return this.Width;
    }

    public void setHeight(int height)
    {
        this.Height = height;
    }

    public void setLength(int length)
    {
        this.Length = length;
    }

    public void setWidth(int width)
    {
        this.Width = width;
    }

    // -------------------------------------------------------

}